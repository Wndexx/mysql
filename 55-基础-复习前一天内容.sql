# 数据库的相关概念

    # 一、数据库的好处
        # 1、可以持久化数据到本地
        # 2、结构化查询

    # 二、数据库的常见概念
        # 1、DB      数据库，存储数据的容器
        # 2、DBMS    数据库管理系统，又称为数据库软件或数据库产品，用于创建或管理 DB
        # 3、SQL     结构化查询语言，用于和数据库通信的语言，不是某个数据库软件特有的，几乎所有的主流的数据库软件通用的语言

    # 三、数据库存储数据的特点
        # 1、数据存放到表中，然后表再放到库中
        # 2、一个库中可以有多张表，每张表具有唯一的表名用来表示自己
        # 3、表中有一个或多个列，列又称为"字段"，相当于 Java 中"属性"
        # 4、表中的每一行数据，相当于 Java 中"对象"

    # 四、常见的数据库管理系统
        # mysql、oracle、db2、sqlserver


