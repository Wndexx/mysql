/*
    - DB

        - 数据库（database）：存储数据的 "仓库"。它保存了一系列有组织的数据

    - DBMS

        - 数据库管理系统（Database Manegement System），简称数据库软件（产品）。数据库是通过 DBMS 创建和操作的

        - 客户端程序通过数据库管理系统 DBMS 对数据库进行检索数据、插入数据、更新数据、删除数据等操作

        - 常见的数据库管理系统：MySQL(oracle)、Oracle（oracle，收费）、DB2（ibm，针对海量数据）、SqlSever（微软，只能安装在 windows 系统）等

        - 分为两类

            - 基于共享文件系统的 DBMS （Access）

            - 基于客户机-服务器（C/S）的 DBMS（MySQL、Oracle、SqlSever）

    - SQL

        - 结构化查询语言（Structure Query Language）：专门用来与数据库软件通信的语言

        - 优点

            - 不是某个特定数据库供应商专有的语言，几乎所有 DBMS 都支持 SQL

            - 简单易学

            - 虽然简单，但实际上是一种强有力的语言，灵活使用其语言元素，可以进行非常复杂和高级的数据库操作

*/