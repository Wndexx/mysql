/*
    - 常见函数

        - 概念：函数类似于 Java 的方法，将一组逻辑语句封装在方法体中，对外暴露方法名

        - 好处

            - 隐藏了实现细节

            - 提高了代码的重用性

        - 调用

            - select 函数名(实参列表) 【from 表】;

        - 特点

            - 叫什么（函数名）

            - 干什么（函数的功能）

        - 分类

            - 单行函数

                - 如 concat、length、ifnull、isnull等

            - 分组函数（又称为统计函数、聚合函数、组函数）

                - 功能：做统计使用
*/